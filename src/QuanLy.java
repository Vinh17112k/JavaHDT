    private void loadData() {
        fileio.read(listC, File_C);
        fileio.read(listN, File_N);
        listC.forEach((t) -> {
            tablec.addRow(t.toObjects());
        });
        listN.forEach((t) -> {
            tablen.addRow(t.toObjects());
        });
    }
    private void loadID() {
        jComboBox2.removeAllItems();
        jComboBox3.removeAllItems();
        listC.forEach((t) -> {
            jComboBox2.addItem(t.getId() + "");
        });
        listN.forEach((t) -> {
            jComboBox3.addItem(t.getId() + "");
        });
    }
    private int dem(String name)
    {
        int time=0;
        for(int i=0;i<listql.size();i++)
        {
            if(listql.get(i).getN().getName().equals(name))
                time+=listql.get(i).getGio();
        }
        return time;
    }
