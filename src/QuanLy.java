    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String gio=jTextField6.getText();
        int idC=jComboBox2.getSelectedIndex();
        int idN=jComboBox3.getSelectedIndex();
        try {
            int G=Integer.parseInt(gio);
            QL q=new QL(listC.get(idC), listN.get(idN), G);
            
            for (int i = 0; i < listql.size(); i++) {
                if (listql.get(i).getC().getName().equals(q.getC().getName()) && listql.get(i).getN().getName().equals(q.getN().getName())) {
                    JOptionPane.showMessageDialog(null, "mot nhan vien khong the lam hai du an cung vi tri");
                    return;
                }
            }
            if(dem(q.getN().getName())+G>240){
                JOptionPane.showMessageDialog(null, "qua so gio");
                return;
            }
            listql.add(q);
            tableql.addRow(q.toObjects());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "du lieu sai dinh dang");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

