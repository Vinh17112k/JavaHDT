    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String loai = jComboBox1.getSelectedItem().toString();
        String gia=jTextField2.getText();
        if (name == "" || loai == "" || gia == "") {
            JOptionPane.showMessageDialog(null, "Nhap thieu thong tin");
            return;
        }
        try {
            int cost=Integer.parseInt(gia);
            int currentPos = listC.size() - 1;
        if (currentPos != -1) {
            CongViec.setCurrentId(listC.get(currentPos).getId());
        }
        CongViec C = new CongViec(cost, name, name);
        for (int i = 0; i < listC.size(); i++) {
            if (listC.get(i).getName().equals(C.getName())) {
                JOptionPane.showMessageDialog(null, "trung nhan vien");
                return;
            }
        }
        listC.add(C);
        tablec.addRow(C.toObjects());
        loadID();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "du lieu sai dinh dang");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

