    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String name = jTextField3.getText();
        String address = jTextField4.getText();
        String pro = jTextField5.getText();
        if (name == "" || address == "" || pro == "") {
            JOptionPane.showMessageDialog(null, "Nhap thieu thong tin");
            return;
        }
        int currentPos = listN.size() - 1;
        if (currentPos != -1) {
            NhanVien.setCurrentId(listN.get(currentPos).getId());
        }
        NhanVien nhanvien = new NhanVien(name, address, pro);
        for (int i = 0; i < listN.size(); i++) {
            if (listN.get(i).getName().equals(nhanvien.getName())) {
                JOptionPane.showMessageDialog(null, "trung nhan vien");
                return;
            }
        }
        listN.add(nhanvien);
        tablen.addRow(nhanvien.toObjects());
        loadID();
    }//GEN-LAST:event_jButton3ActionPerformed

